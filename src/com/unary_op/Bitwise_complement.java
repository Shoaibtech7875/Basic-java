package com.unary_op;

public class Bitwise_complement {

	public static void main(String[] args) {

		int n1 = 9, n2 = -3;
		System.out.println(" n1 : " + n1);
		System.out.println("n2 : " + n2);

		System.out.println(n1 + " Bitwaise Complement : " + ~n1);
		System.out.println(n2 + " Bitwaise Complement : " + ~n2);

	}

}
